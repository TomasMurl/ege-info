alf = "0123456789ABCDE"

for x in alf:
    o1_15 = "123" + x + "5"
    o2_15 = "1" + x + "233"
    o1 = int(o1_15, 15)
    o2 = int(o2_15, 15)
    print(f"x = {x}, o1_15 = {o1_15}, o2_15 = {o2_15}")
    print(f"x = {x}, o1 = {o1}, o2 = {o2}")
    if (o1 + o2) % 14 == 0:
        print((o1 + o2) // 14)
        break